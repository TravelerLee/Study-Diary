List_Study

常用属性
length  list的长度，返回int
reversed   将list反转
isEmpty 是否为空，返回bool
isNotEmpty 是否不为空，返回bool

常用方法：

add 增加一个list参数
addAll 拼接数组
indexOf 查找索引 传入具体值
remove  删除  传入具体值
removeAt 删除 传入索引值
insert(index,value) 在指定位置插入值
insertAll(index,list)  在指定位置插入List
toList()  将其他类型转换为List
join()  List类型转换为字符串
split() 字符串转化成List
forEach()  遍历列表
map() 将数据转换为迭代器元组